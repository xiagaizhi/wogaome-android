public class MyWalletInfo {
    private String lkc;
    private double power;
    private String lkcDescUrl;
    private String lkcDetailUrl;
    private String powerDescUrl;

    public String getLkc() {
        return lkc;
    }

    public void setLkc(String lkc) {
        this.lkc = lkc;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }

    public String getLkcDescUrl() {
        return lkcDescUrl;
    }

    public void setLkcDescUrl(String lkcDescUrl) {
        this.lkcDescUrl = lkcDescUrl;
    }

    public String getLkcDetailUrl() {
        return lkcDetailUrl;
    }

    public void setLkcDetailUrl(String lkcDetailUrl) {
        this.lkcDetailUrl = lkcDetailUrl;
    }

    public String getPowerDescUrl() {
        return powerDescUrl;
    }

    public void setPowerDescUrl(String powerDescUrl) {
        this.powerDescUrl = powerDescUrl;
    }
}
